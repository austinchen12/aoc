sum_ = 0
dp = {}
def recurse(springs, targets, si, ti, count):
    if (si, ti, count) in dp:
        return dp[(si, ti, count)]
	
    if si == len(springs):
        if ti == len(targets) and count == 0:
            return 1
        elif ti == len(targets) - 1 and count == targets[ti]:
            return 1
        else:
            return 0
    
    c = springs[si]
    cases = []
    if c == '?':
        cases.append('.')
        cases.append('#')
    else:
        cases.append(c)
    
    result = 0
    for c in cases:
        if c == '.':
            if ti < len(targets) and count == targets[ti]:
                result += recurse(springs, targets, si + 1, ti + 1, 0)
            elif count == 0:
                result += recurse(springs, targets, si + 1, ti, 0)
        elif c == '#':
            result += recurse(springs, targets, si + 1, ti, count + 1)
    dp[(si, ti, count)] = result
    return dp[(si, ti, count)]
